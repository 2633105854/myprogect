package com.zt.manager;

public class Room_Type {
	private int rt_id;
	private String rt_type;
	private double rt_price;
	private int rt_num;

	public int getRt_id() {
		return rt_id;
	}

	public void setRt_id(int rt_id) {
		this.rt_id = rt_id;
	}

	public String getRt_type() {
		return rt_type;
	}

	public void setRt_type(String rt_type) {
		this.rt_type = rt_type;
	}

	public double getRt_price() {
		return rt_price;
	}

	public void setRt_price(double rt_price) {
		this.rt_price = rt_price;
	}

	public int getRt_num() {
		return rt_num;
	}

	public void setRt_num(int rt_num) {
		this.rt_num = rt_num;
	}

}
