package com.zt.manager;

public class Client {
	private int cl_id;
	private String cl_name;
	private String cl_password;
	private String cl_realname;
	private String cl_card;
	private String cl_phone;
	private String cl_vip;

	public int getCl_id() {
		return cl_id;
	}

	public void setCl_id(int cl_id) {
		this.cl_id = cl_id;
	}

	public String getCl_name() {
		return cl_name;
	}

	public void setCl_name(String cl_name) {
		this.cl_name = cl_name;
	}

	public String getCl_password() {
		return cl_password;
	}

	public void setCl_password(String cl_password) {
		this.cl_password = cl_password;
	}

	public String getCl_realname() {
		return cl_realname;
	}

	public void setCl_realname(String cl_realname) {
		this.cl_realname = cl_realname;
	}

	public String getCl_card() {
		return cl_card;
	}

	public void setCl_card(String cl_card) {
		this.cl_card = cl_card;
	}

	public String getCl_phone() {
		return cl_phone;
	}

	public void setCl_phone(String cl_phone) {
		this.cl_phone = cl_phone;
	}

	public String getCl_vip() {
		return cl_vip;
	}

	public void setCl_vip(String cl_vip) {
		this.cl_vip = cl_vip;
	}

}
